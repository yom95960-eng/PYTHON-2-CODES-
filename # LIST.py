 # LIST

lst = [10, 30, 20, 40]
print("List:", lst)                 # 1

print("First element:", lst[0])     # 2

lst.append(50)                      # 3
print("After append:", lst)

lst.insert(1, 15)                   # 4
print("After insert:", lst)

lst.remove(30)                      # 5
print("After remove:", lst)

lst.sort()                          # 6
print("Sorted list:", lst)


# TUPLE

tup = (5, 10, 15, 20)
print("\nTuple:", tup)              # 7

print("Tuple element:", tup[2])     # 8

print("Tuple slicing:", tup[1:3])   # 9

print("Tuple length:", len(tup))    # 10


# SET

st = {3, 1, 4, 2}
print("\nSet:", st)                 # 11

st.add(5)                           # 12
print("After add:", st)

st.remove(1)                        # 13
print("After remove:", st)

st.add(3)                           # 14
print("After duplicate add:", st)

print("Sorted set:", sorted(st))    # 15

a = {1, 2, 3}
b = {3, 4, 5}
print("Union:", a | b)              # 16
print("Intersection:", a & b)
